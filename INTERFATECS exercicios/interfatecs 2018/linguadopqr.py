def main():
    palavrapq = input().replace('PQ','').split(' ')
    resposta = ""
    for i in range(0,len(palavrapq)):
        
        if palavrapq[i] in "?.,!":
            pass
        elif palavrapq[i] != 'R':
            resposta+=str(palavrapq[i])
        else:
            if palavrapq[i+1] in "?.,!":
                resposta+=str(palavrapq[i+1])
            else:
                resposta+=" "
    print(resposta)
main()